    /**
    * M√©todo para cerrar la sesi√≥n actual y regresar al login
    */
   private void cerrarSesion() {
       try {
           // Confirmar acci√≥n con el usuario
           int respuesta = JOptionPane.showConfirmDialog(
               this,
               "¬øEst√° seguro que desea cerrar la sesi√≥n?",
               "Confirmar cierre de sesi√≥n",
               JOptionPane.YES_NO_OPTION,
               JOptionPane.QUESTION_MESSAGE
           );

           if (respuesta == JOptionPane.YES_OPTION) {
               // Cerrar la sesi√≥n usando el GestorSesion
               GestorSesion.getInstance().cerrarSesion();

               // Mostrar mensaje de confirmaci√≥n
               mostrarExito("Sesi√≥n cerrada correctamente");

               // Regresar al panel de login
               regresarAlLogin();
           }

       } catch (Exception e) {
           mostrarError("Error al cerrar sesi√≥n: " + e.getMessage());
           System.err.println("Error en cerrar sesi√≥n: " + e.getMessage());
       }
   }

   /**
    * Regresa al panel de login
    */
    private void regresarAlLogin() {
        try {
            java.awt.Container parent = this.getParent();
            while (parent != null && !(parent instanceof FrmPrincipal2)) {
                parent = parent.getParent();
            }

            if (parent instanceof FrmPrincipal2) {
                FrmPrincipal2 ventanaPrincipal = (FrmPrincipal2) parent;
                ventanaPrincipal.mostrarLogin(); // Usar el m√©todo centralizado
            }
        } catch (Exception e) {
            mostrarError("Error al regresar al login: " + e.getMessage());
        }
    }
   private void mostrarError(String mensaje) {
        JOptionPane.showMessageDialog(this, mensaje, "Error", JOptionPane.ERROR_MESSAGE);
    }

    private void mostrarExito(String mensaje) {
        JOptionPane.showMessageDialog(this, mensaje, "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
    }
    
    /**
    * M√©todo alternativo para cerrar sesi√≥n sin confirmaci√≥n (para casos especiales)
    */
   private void cerrarSesionDirecto() {
       try {
           // Cerrar sesi√≥n directamente
           GestorSesion.getInstance().cerrarSesion();

           // Regresar al login
           regresarAlLogin();

       } catch (Exception e) {
           mostrarError("Error al cerrar sesi√≥n: " + e.getMessage());
       }
   }

   /**
    * Verificar si hay sesi√≥n activa antes de permitir operaciones
    */
   private boolean verificarSesionActiva() {
       if (!GestorSesion.getInstance().haySesionActiva()) {
           mostrarError("Su sesi√≥n ha expirado. Por favor, inicie sesi√≥n nuevamente.");
           regresarAlLogin();
           return false;
       }
       return true;
   }

